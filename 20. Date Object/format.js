let currDate = new Date();

console.log(currDate.toString());//India standard full time
console.log(currDate.toISOString());//International Time

console.log(currDate.toDateString());//day month date year

console.log(currDate.toLocaleString());//Date and time
console.log(currDate.toLocaleDateString());//only Date
console.log(currDate.toLocaleTimeString());//only Time
